  <h1 class="text is-size-1"> What is PeopleMap? </h1>
  <p class="text is-size-5"> Finding relevant scientists for research projects can often be difficult to achieve. The most common techniques of finding researchers are generally inefficient or ineffective. This results in lost opportunities to develop new interests and make connections outside of an existing network of colleagues. Furthermore, resources such as university directories often lack the information necessary to understand a researcher's interests and past work, making it harder to pinpoint potential collaborators. Overall, this leads to an ineffective process for exploring the diversity of researchers at an institution, hindering the ability of both external and internal entities to find possible researchers for their upcoming projects.
  </p>
  <p class="text is-size-5"> To solve this problem, we have developed PeopleMap, an interactive, web-based tool that uses natural language processing (NLP) techniques to improve the accuracy of the information and characterization of researchers. With a host of tools for exploring publication datasets, PeopleMap is the first tool to visualize embeddings representing the combined publications of individual researchers in an explorable, graphical representation. Altogether, PeopleMap both improves the presentation of publication information as well as adds compelling insights into the works of the researchers at academic institutions.
  </p>
